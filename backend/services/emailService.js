  // Ki·ªÉm tra v√† g·ª≠i email nh·∫Øc nh·ªü cho t·∫•t c·∫£ subscriptions c·∫ßn thi·∫øt
  async checkAndSendReminderEmails() {
    try {
      console.log('üîç Checking for subscriptions that need reminders...');

      // T√¨m t·∫•t c·∫£ subscriptions c·∫ßn g·ª≠i reminder
      const subscriptions = await Subscription.find({ isActive: true })
        .populate('userId', 'fullName email isActive');

      if (!subscriptions.length) {
        console.log('üì≠ No active subscriptions found');
        return { processed: 0, sent: 0, failed: 0 };
      }

      let processed = 0;
      let sent = 0;
      let failed = 0;

      for (const subscription of subscriptions) {
        processed++;

        // Ki·ªÉm tra user c√≤n active kh√¥ng
        if (!subscription.userId || !subscription.userId.isActive) {
          console.log(`‚è≠Ô∏è Skipping inactive user for subscription ${subscription._id}`);
          continue;
        }

        // Ki·ªÉm tra c√≥ c·∫ßn g·ª≠i reminder kh√¥ng
        if (!subscription.needsReminder()) {
          continue;
        }

        console.log(`üìß Sending reminder for subscription: ${subscription.serviceName} to ${subscription.userId.email}`);

        // G·ª≠i email
        const result = await this.sendReminderEmail(subscription.userId, subscription);

        if (result.success) {
          // ƒê√°nh d·∫•u ƒë√£ g·ª≠i reminder
          await subscription.markReminderSent();
          sent++;
          console.log(`‚úÖ Reminder sent successfully for ${subscription.serviceName}`);
        } else {
          failed++;
          console.error(`‚ùå Failed to send reminder for ${subscription.serviceName}:`, result.error);
        }

        // Delay nh·ªè ƒë·ªÉ tr√°nh spam
        await new Promise(resolve => setTimeout(resolve, 1000));
      }

      const summary = { processed, sent, failed };
      console.log('üìä Email reminder summary:', summary);
      
      return summary;

    } catch (error) {
      console.error('‚ùå Error in checkAndSendReminderEmails:', error);
      throw error;
    }
  }

